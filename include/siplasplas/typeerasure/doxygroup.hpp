/**
 * \defgroup type-erasure
 * \brief Type-erasure utilities
 *
 * This module provides classes and methods to manipulate type-erased objects:
 *
 *  - `cpp::SimpleAny`: Type-erased value container with customizable storage
 *  - `cpp::typeerasure::Function`: Type-erased callable with customizable storage
 *  - `cpp::Any`: Type-erased value container with a customizable object-like interface
 *  at runtime.
 *  - `cpp::typeerasure::invoke()`: Generic callable invocation with type-erased arguments
 */
